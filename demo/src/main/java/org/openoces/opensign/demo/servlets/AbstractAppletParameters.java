package org.openoces.opensign.demo.servlets;

import org.openoces.opensign.demo.utils.Base64;

import java.io.Serializable;

/**
 * @author Paw Figgé Kjeldgaard (pfk@miracleas.dk)
 */
public abstract class AbstractAppletParameters implements Serializable {
    private String appletVersion;
    private Integer appletWidth;
    private Integer appletHeight;
    private String logonto;
    private String signproperties;
    private String cdkortservice;
    private String appletLocale;
    private boolean showSsid;
    private String optionalid;
    private String loglevel;
    private String subjectdnfilter;
    private String issuerdnfilter;
    private String sernofilter;
    private boolean liveconnect;
    private String[] keystores;
    private String signatureAlgorithm;
    private boolean virklogon;
    private String inputstyle;
    private boolean allowregistryaccess;
    private boolean sortcertificates;
    private String logonheader;
    private boolean appletRequestOnMac;

    public String getAppletVersion() {
        return appletVersion;
    }

    public void setAppletVersion(String appletVersion) {
        this.appletVersion = appletVersion;
    }

    public Integer getAppletWidth() {
        return appletWidth;
    }

    public void setAppletWidth(Integer appletWidth) {
        this.appletWidth = appletWidth;
    }

    public Integer getAppletHeight() {
        return appletHeight;
    }

    public void setAppletHeight(Integer appletHeight) {
        this.appletHeight = appletHeight;
    }

    public String getLogonto() {
        return logonto;
    }

    public void setLogonto(String logonto) {
        this.logonto = logonto;
    }

    public String getSignproperties() {
        return signproperties;
    }

    public void setSignproperties(String signproperties) {
        this.signproperties = signproperties;
    }

    public String getCdkortservice() {
        return cdkortservice;
    }

    public void setCdkortservice(String cdkortservice) {
        this.cdkortservice = cdkortservice;
    }

    public String getAppletLocale() {
        return appletLocale;
    }

    public void setAppletLocale(String appletLocale) {
        this.appletLocale = appletLocale;
    }

    public boolean getShowSsid() {
        return showSsid;
    }

    public void setShowSsid(boolean showSsid) {
        this.showSsid = showSsid;
    }

    public String getOptionalid() {
        return optionalid;
    }

    public void setOptionalid(String optionalid) {
        this.optionalid = optionalid;
    }

    public String getLoglevel() {
        return loglevel;
    }

    public void setLoglevel(String loglevel) {
        this.loglevel = loglevel;
    }

    public String getSubjectdnfilter() {
        return subjectdnfilter;
    }

    public String getBase64Subjectdnfilter() {
        return Base64.base64Encode(subjectdnfilter);
    }

    public void setSubjectdnfilter(String subjectdnfilter) {
        this.subjectdnfilter = subjectdnfilter;
    }

    public String getIssuerdnfilter() {
        return issuerdnfilter;
    }

    public String getBase64Issuerdnfilter() {
        return Base64.base64Encode(issuerdnfilter);
    }

    public void setIssuerdnfilter(String issuerdnfilter) {
        this.issuerdnfilter = issuerdnfilter;
    }

    public String getSernofilter() {
        return sernofilter;
    }

    public String getBase64Sernofilter() {
        return Base64.base64Encode(sernofilter);
    }

    public void setSernofilter(String sernofilter) {
        this.sernofilter = sernofilter;
    }

    public boolean isLiveconnect() {
        return liveconnect;
    }

    public void setLiveconnect(boolean liveconnect) {
        this.liveconnect = liveconnect;
    }

    public String[] getKeystores() {
        return keystores;
    }

    public void setKeystores(String[] keystores) {
        this.keystores = keystores;
    }

    public String getSignatureAlgorithm() {
        return signatureAlgorithm;
    }

    public void setSignatureAlgorithm(String signatureAlgorithm) {
        this.signatureAlgorithm = signatureAlgorithm;
    }

    public boolean isVirklogon() {
        return virklogon;
    }

    public void setVirklogon(boolean virklogon) {
        this.virklogon = virklogon;
    }

    public String getInputstyle() {
        return inputstyle;
    }

    public void setInputstyle(String inputstyle) {
        this.inputstyle = inputstyle;
    }

    public boolean isAllowregistryaccess() {
        return allowregistryaccess;
    }

    public void setAllowregistryaccess(boolean allowregistryaccess) {
        this.allowregistryaccess = allowregistryaccess;
    }

    public boolean isSortcertificates() {
        return sortcertificates;
    }

    public void setSortcertificates(boolean sortcertificates) {
        this.sortcertificates = sortcertificates;
    }

    public String getLogonheader() {
        return logonheader;
    }

    public void setLogonheader(String logonheader) {
        this.logonheader = logonheader;
    }

    public boolean isAppletRequestOnMac() {
        return appletRequestOnMac;
    }

    public void setAppletRequestOnMac(boolean appletRequestOnMac) {
        this.appletRequestOnMac = appletRequestOnMac;
    }

}