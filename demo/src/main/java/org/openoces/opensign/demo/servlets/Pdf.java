package org.openoces.opensign.demo.servlets;

import org.springframework.web.multipart.commons.CommonsMultipartFile;

import java.io.Serializable;

/**
 * @author Paw Figgé Kjeldgaard (pfk@miracleas.dk)
 */
public class Pdf implements Serializable {
    private byte[] content;
    private String contentType;
    private String name;

    private String pdfUri;
    private String pdfHashValue;
    private String pdfHashAlgorithm;

    public Pdf(CommonsMultipartFile file) {
        this.name = file.getOriginalFilename();
        this.content = file.getBytes();
        this.contentType = file.getContentType();
    }

    public byte[] getContent() {
        return content;
    }

    public void setContent(byte[] content) {
        this.content = content;
    }

    public String getContentType() {
        return contentType;
    }

    public void setContentType(String contentType) {
        this.contentType = contentType;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getPdfUri() {
        return pdfUri;
    }

    public void setPdfUri(String pdfUri) {
        this.pdfUri = pdfUri;
    }

    public String getPdfHashValue() {
        return pdfHashValue;
    }

    public void setPdfHashValue(String pdfHashValue) {
        this.pdfHashValue = pdfHashValue;
    }

    public String getPdfHashAlgorithm() {
        return pdfHashAlgorithm;
    }

    public void setPdfHashAlgorithm(String pdfHashAlgorithm) {
        this.pdfHashAlgorithm = pdfHashAlgorithm;
    }
}
