package org.openoces.opensign.client.applet.dialogs.listeners;

/**
 * @author Paw Figg√© Kjeldgaard (pfk@miracleas.dk)
 */
public class StringSignText implements SignText {
    private String signText;

    public StringSignText(String signText) {
        this.signText = signText;
    }

    public String getSignText() {
        return signText;
    }

    @Override
    public String getMimeType() {
        return "text/plain";
    }

    @Override
    public boolean equals(Object o) {
        if (this == o) return true;
        if (o == null || getClass() != o.getClass()) return false;

        StringSignText that = (StringSignText) o;

        return signText.equals(that.signText);

    }

    @Override
    public int hashCode() {
        return signText.hashCode();
    }
}
