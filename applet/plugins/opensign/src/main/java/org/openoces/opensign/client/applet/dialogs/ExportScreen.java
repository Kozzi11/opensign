package org.openoces.opensign.client.applet.dialogs;

import org.openoces.opensign.certificate.CertificateHandler;
import org.openoces.opensign.client.applet.CallBackHandler;
import org.openoces.opensign.client.applet.dialogs.panel.AbstractPanel;
import org.openoces.opensign.client.applet.dialogs.panel.ExportPanel;

public class ExportScreen extends AbstractScreen {
    private ExportPanel panel;

    public ExportScreen(CallBackHandler callBackHandler, java.util.List<CertificateHandler> certificates, boolean supportBrowsingForCertificate) {
        super(callBackHandler, certificates, supportBrowsingForCertificate);
    }

    @Override
    protected AbstractPanel getPanel() {
        return panel;
    }

    @Override
    protected void initialize() {
        if(panel == null) panel = new ExportPanel(callBackHandler, certificates, supportBrowsingForCertificate);
    }
}
