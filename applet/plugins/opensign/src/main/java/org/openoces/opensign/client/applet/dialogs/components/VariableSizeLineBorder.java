package org.openoces.opensign.client.applet.dialogs.components;

import javax.swing.border.AbstractBorder;
import java.awt.*;

public class VariableSizeLineBorder extends AbstractBorder {
    private int lineThickness, totalThickness;
	private Color lineColor;

	public VariableSizeLineBorder(int lineThickness, int totalThickness, Color lineColor) {
		this.lineThickness = lineThickness;
		this.totalThickness = totalThickness;
		this.lineColor = lineColor;
	}

	public void paintBorder(Component c, Graphics g, int x, int y, int width, int height) {
		Color oldColor = g.getColor();
		int i = totalThickness - lineThickness;

		g.setColor(lineColor);
		for (i = 0; i < lineThickness; i++) {
			g.drawRect(x + i, y + i, width - i - i - 1, height - i - i - 1);
		}
		g.setColor(oldColor);
	}

	/**
	 * Returns the insets of the border.
	 *
	 * @param c
	 *            the component for which this border insets value applies
	 */
	public Insets getBorderInsets(Component c) {
		return new Insets(totalThickness * 2, totalThickness * 2, totalThickness * 2, totalThickness * 2);
	}

	/**
	 * Reinitialize the insets parameter with this Border's current Insets.
	 *
	 * @param c
	 *            the component for which this border insets value applies
	 * @param insets
	 *            the object to be reinitialized
	 */
	public Insets getBorderInsets(Component c, Insets insets) {
		insets.left = insets.top = insets.right = insets.bottom = totalThickness * 2;
		return insets;
	}
}
