package org.openoces.opensign.client.applet.dialogs.components;

import javax.swing.*;
import java.awt.*;

public class OpensignTextField extends JTextField {
    private boolean pasteAllowed;

    public OpensignTextField(Font font) {
        super();
        setOpaque(true);

        setFont(font);
        setDragEnabled(false);
        setDropTarget(null);
        setRequestFocusEnabled(true);
        setFocusable(true);
    }

    public final void setAllowPaste(boolean allowed) {
		this.pasteAllowed = allowed;
	}

    public final void setFont(Font f) {
		super.setFont(f);
	}

	public final void setForeground(Color fg) {
		super.setForeground(fg);
	}


	public final Dimension getPreferredSize() {
		Dimension d = super.getPreferredSize();
		d.height = 20;
		return d;
	}

	public final Dimension getSize() {
		Dimension d = super.getSize();
		d.height = 20;
		return d;
	}

	public final Dimension getMinimumSize() {
		return getPreferredSize();
	}

	public final Insets getInsets() {
		return new Insets( 1, 3, 1 ,1 );
	}

    public final void paintImmediately(int x, int y, int w, int h) {
		super.paintImmediately(0, 0, getWidth(), getHeight());
	}

	public final void paintImmediately(Rectangle r) {
		super.paintImmediately(new Rectangle(0, 0, getWidth(), getHeight()));
	}

	public final void copy() {
		//Not allowed
	}

	public final void cut() {
		//Not allowed
	}

	public final void paste() {
		if (pasteAllowed) {
			super.paste();
		}
	}
}