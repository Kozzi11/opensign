package org.openoces.opensign.certificate.support.suncrypto;

import java.net.URL;
import java.util.Map;

/**
 * @author Paw Figg√© Kjeldgaard (pfk@miracleas.dk)
 */
public class FakeHttpsClientFactory implements HttpsClientFactory {

    @Override
    public HttpsClient create(SunCryptoSupport sunCryptoSupport, URL url, Map<String, String> params) {
        HttpsClient httpsClient = new DefaultHttpsClient(sunCryptoSupport, url, params);
        httpsClient.setUrlConnectionFactory(new FakeURLConnectionFactory());
        return httpsClient;
    }
}
