package org.openoces.opensign.certificate.plugin.cdcard.dialogs;

/**
 * This class represents the dialog displayed when the import dialog is being displayed.
 *
 * @author Preben Valeur  <prv@tdc.dk>
 * @author Paw F. Kjeldgaard <pakj@danid.dk>
 */

import org.openoces.opensign.client.applet.CallBackHandler;
import org.openoces.opensign.client.applet.dialogs.AbstractDialog;
import org.openoces.opensign.client.applet.dialogs.panel.AbstractPanel;

import javax.swing.*;

public class DlgImport extends AbstractDialog {
    private ImportPanel importPanel;

    private String title;
    private String param;

    public DlgImport(final CallBackHandler parent, JComponent oldFocusComponent, String title, String param) {
        super(parent, oldFocusComponent, 400, 200);
        this.title = title;
        this.param = param;
    }

    @Override
    protected final void initialize() {
        if(importPanel == null) importPanel = new ImportPanel(callBackHandler, this, title, param);
    }

    @Override
    protected final AbstractPanel getPanel() {
        return importPanel;
    }




}