package org.openoces.opensign.pdf.validator.client;

import javax.swing.*;

/**
 * @author: Michael Martinsen <mma@openminds.dk>
 */
public class IconManager {

    public static final String IMAGE_THUMBS_UP_PATH = "/images/thumbs_up.png";
    public static final String IMAGE_THUMBS_DOWN_PATH = "/images/thumbs_down.png";

    ImageIcon getIconBasedOnState(boolean state) {
        if (state) {
            return getValidationOkIcon();
        } else {
            return getValidationErrorIcon();
        }
    }

    private ImageIcon getValidationOkIcon() {
        return createImageIcon(IMAGE_THUMBS_UP_PATH);
    }

    private ImageIcon getValidationErrorIcon() {
        return createImageIcon(IMAGE_THUMBS_DOWN_PATH);
    }

    private ImageIcon createImageIcon(String path) {
        java.net.URL imgURL = getClass().getResource(path);
        if (imgURL != null) {
            return new ImageIcon(imgURL, "");
        } else {
            System.err.println("Couldn't find file: " + path);
            return null;
        }
    }
}
